const passwordOne = document.getElementById("password-el-1");
const passwordTwo = document.getElementById("password-el-2");

function genPassword(length) {
  passwordOne.textContent = "";
  passwordTwo.textContent = "";

  for (let i = 0; i < length; i++) {
    let randomChars1 = Math.floor(Math.random() * characters.length);
    let randomChars2 = Math.floor(Math.random() * characters.length);
    passwordOne.textContent += characters[randomChars1];
    passwordTwo.textContent += characters[randomChars2];
    
  }
}


